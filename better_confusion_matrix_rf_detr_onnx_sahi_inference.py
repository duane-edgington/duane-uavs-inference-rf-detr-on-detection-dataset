def match_detections(ground_truth, predictions, iou_threshold=0.5):
    """
    Match ground truth boxes to predicted boxes using the Hungarian algorithm
    (optimal assignment) instead of greedy matching, fixing Bug #1.
    """
    if len(ground_truth) == 0 and len(predictions) == 0:
        return np.array([], dtype=int), np.array([], dtype=int), np.array([], dtype=int)
    elif len(ground_truth) == 0:
        return np.array([], dtype=int), np.array([], dtype=int), np.arange(len(predictions), dtype=int)
    elif len(predictions) == 0:
        return np.arange(len(ground_truth), dtype=int), np.array([], dtype=int), np.array([], dtype=int)

    ground_truth_boxes = torch.from_numpy(ground_truth.xyxy)
    predictions_boxes  = torch.from_numpy(predictions.xyxy)
    iou_matrix = torchvision.ops.box_iou(ground_truth_boxes, predictions_boxes).numpy()

    # Bug #1 fix: use Hungarian algorithm for optimal (non-greedy) assignment
    gt_indices, pred_indices = linear_sum_assignment(-iou_matrix)

    matched_ground_truth_indices = []
    matched_prediction_indices   = []
    matched_pred_set             = set()

    for gt_idx, pred_idx in zip(gt_indices, pred_indices):
        if iou_matrix[gt_idx, pred_idx] >= iou_threshold:
            matched_ground_truth_indices.append(gt_idx)
            matched_prediction_indices.append(pred_idx)
            matched_pred_set.add(pred_idx)

    false_positive_indices = [j for j in range(len(predictions)) if j not in matched_pred_set]

    return (
        np.array(matched_ground_truth_indices, dtype=int),
        np.array(matched_prediction_indices,   dtype=int),
        np.array(false_positive_indices,        dtype=int),
    )


