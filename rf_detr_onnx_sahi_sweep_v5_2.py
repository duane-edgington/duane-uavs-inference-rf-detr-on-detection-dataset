# The RF-DETR ONNX classification head has one logit column per class the model was
# TRAINED with, which is not necessarily 1 even for a single-class dataset (this model
# exports [.,300,2] — likely a reserved/offset column). For single-class evaluation we
# read one column and label every detection as class 0. Column 0 reproduces the original
# notebook's behaviour; set this to whichever column corresponds to your object if 0 is
# not correct (see the note printed at inference time).
OBJECT_CLASS_COLUMN = 0


class RFDetrOnnxDetectionModel(DetectionModel):
    """SAHI wrapper for RF-DETR ONNX.
    ONNX outputs (rfdetr 1.5.1):
      dets:   [1, 300, 4]  normalised cx,cy,w,h
      labels: [1, 300, 1]  raw logits  (sigmoid → confidence)
    """

    def load_model(self):
        providers = (
            ['CUDAExecutionProvider', 'CPUExecutionProvider']
            if torch.cuda.is_available() else ['CPUExecutionProvider']
        )
        self.session      = ort.InferenceSession(self.model_path, providers=providers)
        self.input_name   = self.session.get_inputs()[0].name
        self.output_names = [o.name for o in self.session.get_outputs()]
        print(f'ONNX inputs : {self.input_name}')
        print(f'ONNX outputs: {self.output_names}')
        print(f'Active provider: {self.session.get_providers()[0]}')
        self.model = self.session

    def perform_inference(self, image: np.ndarray):
        h, w  = image.shape[:2]
        tile  = Image.fromarray(image).resize((self.image_size, self.image_size), Image.BILINEAR)
        tile  = np.array(tile, dtype=np.float32) / 255.0
        mean  = np.array([0.485, 0.456, 0.406], dtype=np.float32)
        std   = np.array([0.229, 0.224, 0.225], dtype=np.float32)
        tile  = ((tile - mean) / std).transpose(2, 0, 1)[None]
        self._original_predictions = self.session.run(self.output_names, {self.input_name: tile})
        # Single-class evaluation: we read OBJECT_CLASS_COLUMN from the label head and
        # label every surviving detection as class 0. Warn once (not fatal) if the head
        # has more than one column, so a wrong column choice can't pass silently. If the
        # model is genuinely multi-class, use the multi-class notebook instead.
        n_logit_cls = self._original_predictions[1].shape[-1]
        if not getattr(self, '_class_dim_checked', False):
            self._class_dim_checked = True
            if n_logit_cls != 1:
                print(f'[INFO] ONNX label head has {n_logit_cls} class columns; using '
                      f'column {OBJECT_CLASS_COLUMN} as the object class and labelling '
                      f'all detections class 0. Set OBJECT_CLASS_COLUMN if that is wrong.')
        self._tile_hw = (h, w)

    def convert_original_predictions(self, shift_amount=None, full_shape=None):
      if shift_amount is None:
          shift_amount = [0, 0]

      dets   = self._original_predictions[0][0]   # [300, 4]  cx,cy,w,h normalised
      labels = self._original_predictions[1][0]   # [300, 1]  raw logits
      tile_h, tile_w = self._tile_hw

      # Confidence = sigmoid(logit) for the selected single-class column
      scores = 1.0 / (1.0 + np.exp(-labels[:, OBJECT_CLASS_COLUMN]))   # [300]

      object_prediction_list = []
      for box, score in zip(dets, scores):
          if score < self.confidence_threshold:
              continue
          cx, cy, bw, bh = box
          x1 = (cx - bw / 2) * tile_w
          y1 = (cy - bh / 2) * tile_h
          x2 = (cx + bw / 2) * tile_w
          y2 = (cy + bh / 2) * tile_h

          # Clip to tile boundaries
          x1, y1 = max(0.0, x1), max(0.0, y1)
          x2, y2 = min(float(tile_w), x2), min(float(tile_h), y2)

          if x2 <= x1 or y2 <= y1:
              continue

          # Apply tile offset to convert tile-local → full image coordinates
          x1 += shift_amount[0]
          y1 += shift_amount[1]
          x2 += shift_amount[0]
          y2 += shift_amount[1]

          category_name = self.category_mapping.get("0", "object")
          object_prediction_list.append(
              ObjectPrediction(
                  bbox=[x1, y1, x2, y2],
                  score=float(score),
                  category_id=0,
                  category_name=category_name,
                  shift_amount=[0, 0],   # already applied above
                  full_shape=full_shape,
              )
          )
      self._object_prediction_list_per_image = [object_prediction_list]


def sahi_result_to_sv_detections(result) -> sv.Detections:
    """Convert SAHI PredictionResult → supervision.Detections."""
    preds = result.object_prediction_list
    if not preds:
        return sv.Detections.empty()
    xyxy       = np.array([[p.bbox.minx, p.bbox.miny, p.bbox.maxx, p.bbox.maxy]
                            for p in preds], dtype=np.float32)
    confidence = np.array([p.score.value for p in preds], dtype=np.float32)
    class_ids  = np.array([p.category.id  for p in preds], dtype=int)
    return sv.Detections(xyxy=xyxy, confidence=confidence, class_id=class_ids)


