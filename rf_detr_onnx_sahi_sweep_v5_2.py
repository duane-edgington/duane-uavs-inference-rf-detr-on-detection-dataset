def _extract_tar(src, dest):
    """Extract tar into dest, then collapse one level of unwanted nesting.
    If the tar contains a single top-level directory whose name matches
    the dest basename (e.g. extracting images.tar.gz into .../images/
    yields .../images/images/), its contents are moved up one level."""
    os.makedirs(dest, exist_ok=True)
    with tarfile.open(src) as t:
        try:
            t.extractall(dest, filter='data')   # Python >= 3.12
        except TypeError:
            t.extractall(dest)

    # Collapse nesting: if dest contains exactly one subdir with the
    # same name as dest's basename, move its contents up.
    dest_basename = os.path.basename(dest.rstrip('/'))
    nested = os.path.join(dest, dest_basename)
    if os.path.isdir(nested):
        print(f'  Collapsing nested {dest_basename}/{dest_basename}/ ...')
        for item in os.listdir(nested):
            shutil.move(os.path.join(nested, item), os.path.join(dest, item))
        os.rmdir(nested)


def _find_split_root(base, split):
    """Return the dir that directly contains <split>/, tolerating one level of
    nesting (e.g. base/split OR base/images/split)."""
    if os.path.isdir(os.path.join(base, split)):
        return base
    if os.path.isdir(base):
        for sub in sorted(os.listdir(base)):
            if os.path.isdir(os.path.join(base, sub, split)):
                return os.path.join(base, sub)
    return base


def _has_images(d):
    return os.path.isdir(d) and any(
        f.lower().endswith(IMAGE_EXTS) for f in os.listdir(d))


def _pick_split(images_base, preferred):
    order = [preferred] + [s for s in ('test', 'val', 'train') if s != preferred]
    for s in order:
        if _has_images(os.path.join(images_base, s)):
            return s
    return preferred   # nothing populated; keep preferred (likely empty)


